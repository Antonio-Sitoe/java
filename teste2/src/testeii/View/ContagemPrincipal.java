        private void jComboBox1ActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_jComboBox1ActionPerformed
                String itemSelecionado = (String) jComboBox1.getSelectedItem();
                itemSelecionadoTemporario = itemSelecionado;
                var novoArray = Contagem.filterCandidatos(itemSelecionado);

                DefaultTableModel model = (DefaultTableModel) JTable.getModel();

                if (novoArray != null) {
                        model.setNumRows(0);
                        novoArray.forEach(data -> {
                                model.addRow(new Object[] { data.getNome(), data.getBI(), data.getVotos(),
                                                data.municipio.getNome() });
                        });
                }
        }// GEN-LAST:event_jComboBox1ActionPerformed

        private void BTNVotarActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_BTNVotarActionPerformed
                if (candidatoTemporario != null) {
                        var votos = Contagem.VotarUmCandidato(candidatoTemporario);
                        JVotos.setText(Double.toString(votos));
                        if (itemSelecionadoTemporario != null) {
                                Contagem.atualizaJtable(JTable, itemSelecionadoTemporario);
                        }
                }
        }

        private void JTableMousePressed(java.awt.event.MouseEvent evt) {// GEN-FIRST:event_JTableMousePressed
                // Obtém a linha e coluna clicada
                int row = JTable.rowAtPoint(evt.getPoint());

                String nome = JTable.getValueAt(row, 0).toString();
                var candidato = Contagem.mostrarOCandidato(nome);
                candidatoTemporario = candidato;
                Jname.setText(nome);
                Jmunicipio.setText(candidato.municipio.getNome());
                Jbi.setText(Double.toString(candidato.getBI()));
                JVotos.setText(Double.toString(candidato.getVotos()));
                Jrecenciados.setText(Double.toString(candidato.municipio.getQuantidadeDeRecenciado()));
        }// GEN-LAST:event_JTableMousePressed

        private void JTableKeyReleased(java.awt.event.KeyEvent evt) {// GEN-FIRST:event_JTableKeyReleased
                // TODO add your handling code here:
                int row = JTable.getSelectedRow();

                String nome = JTable.getValueAt(row, 0).toString();
                var candidato = Contagem.mostrarOCandidato(nome);
                candidatoTemporario = candidato;

                Jname.setText(nome);
                Jmunicipio.setText(candidato.municipio.getNome());
                Jbi.setText(Double.toString(candidato.getBI()));
                JVotos.setText(Double.toString(candidato.getVotos()));
                Jrecenciados.setText(Double.toString(candidato.municipio.getQuantidadeDeRecenciado()));
        }// GEN-LAST:event_JTableKeyReleased


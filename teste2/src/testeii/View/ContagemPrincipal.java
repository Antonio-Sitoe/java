        private void VotoNuloActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_VotoNuloActionPerformed
                // TODO add your handling code here:
                if (candidatoTemporario != null) {
                        var votos = Contagem.VotoNulo(candidatoTemporario);
                        JVotos.setText(Double.toString(votos));
                        if (itemSelecionadoTemporario != null) {
                                Contagem.atualizaJtable(JTable, itemSelecionadoTemporario);
                        }
                }
        }// GEN-LAST:event_VotoNuloActionPerformed


    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        var nome = nomeDeCandidato.getText();
        var numeroDeBi = NumeroDeBI.getText();
        String itemSelecionado = (String) Municipio.getSelectedItem();

        try {
            if (nome != null & numeroDeBi != null) {
                var bi = Double.parseDouble(NumeroDeBI.getText());
                Contagem.setCandidatos(nome, bi, itemSelecionado);
            } else {
                Contagem.setCandidatos(nome, 1120120365, itemSelecionado);
            }
        } catch (Exception ex) {
        } finally {
            nomeDeCandidato.setText("");
            NumeroDeBI.setText("");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

